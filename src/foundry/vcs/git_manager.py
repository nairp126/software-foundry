"""Git repository management for the Autonomous Software Foundry.

This module provides comprehensive Git integration including:
- Repository initialization and configuration
- Atomic commits with conventional commit messages
- Branch management for feature development
- File change tracking
- Merge conflict detection and handling
- Tag generation for releases
"""

import os
import subprocess
import logging
from typing import List, Optional, Dict, Tuple
from datetime import datetime
from pathlib import Path
from enum import Enum

logger = logging.getLogger(__name__)


class CommitType(str, Enum):
    """Conventional commit types."""
    FEAT = "feat"
    FIX = "fix"
    REFACTOR = "refactor"
    DOCS = "docs"
    TEST = "test"
    CHORE = "chore"
    STYLE = "style"
    PERF = "perf"


class MergeStrategy(str, Enum):
    """Merge strategies for conflict resolution."""
    OURS = "ours"
    THEIRS = "theirs"
    RECURSIVE = "recursive"


class GitManager:
    """Manages Git operations for autonomous software development."""

    def __init__(self, repo_path: str):
        """Initialize Git manager for a repository.
        
        Args:
            repo_path: Path to the Git repository
        """
        self.repo_path = Path(repo_path).resolve()
        self.default_user_name = "Software Foundry"
        self.default_user_email = "foundry@autonomous.local"

    def _run_git_command(
        self, 
        args: List[str], 
        check: bool = True,
        capture_output: bool = True
    ) -> subprocess.CompletedProcess:
        """Run a Git command in the repository.
        
        Args:
            args: Git command arguments
            check: Whether to raise exception on non-zero exit
            capture_output: Whether to capture stdout/stderr
            
        Returns:
            CompletedProcess instance
            
        Raises:
            subprocess.CalledProcessError: If command fails and check=True
        """
        try:
            result = subprocess.run(
                ["git"] + args,
                cwd=str(self.repo_path),
                check=check,
                capture_output=capture_output,
                text=True
            )
            return result
        except subprocess.CalledProcessError as e:
            logger.error(f"Git command failed: {' '.join(args)}")
            logger.error(f"Error: {e.stderr}")
            raise

    def initialize_repository(
        self,
        initial_branch: str = "main",
        create_gitignore: bool = True,
        create_gitattributes: bool = True
    ) -> bool:
        """Initialize a Git repository with proper configuration.
        
        Implements Requirement 18.1: Initialize Git repository with .gitignore,
        .gitattributes, and initial commit.
        
        Args:
            initial_branch: Name of the initial branch (default: main)
            create_gitignore: Whether to create a default .gitignore
            create_gitattributes: Whether to create a default .gitattributes
            
        Returns:
            True if initialization succeeded, False otherwise
        """
        try:
            # Check if already a git repository
            if (self.repo_path / ".git").exists():
                logger.info(f"Repository already initialized at {self.repo_path}")
                return True

            # Initialize repository
            self._run_git_command(["init", "-b", initial_branch])
            logger.info(f"Initialized Git repository at {self.repo_path}")

            # Configure user
            self._configure_user()

            # Create .gitignore
            if create_gitignore:
                self._create_gitignore()

            # Create .gitattributes
            if create_gitattributes:
                self._create_gitattributes()

            # Create initial commit
            if self._has_changes():
                self.create_commit(
                    CommitType.CHORE,
                    "initialize repository with configuration files"
                )

            return True

        except Exception as e:
            logger.error(f"Failed to initialize repository: {e}")
            return False

    def _configure_user(self) -> None:
        """Configure Git user for the repository."""
        self._run_git_command(
            ["config", "user.name", self.default_user_name],
            check=False
        )
        self._run_git_command(
            ["config", "user.email", self.default_user_email],
            check=False
        )

